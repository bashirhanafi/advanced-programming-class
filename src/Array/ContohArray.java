public class ContohArray {
    public static void main(String[] args) {
       int[] age = new int[5];      // tipedata[] namaArray = new tipedata[banyaknya nilai array]
       System.out.println(age[0]);  // nilai ke-1
       System.out.println(age[1]);  // nilai ke-2
       System.out.println(age[2]);  // ...
       System.out.println(age[3]);  // ...
       System.out.println(age[4]);  // ...
       System.out.println(age[5]);  // nilai ke-5
    }
}

