public class ForLoop12 {
    public static void main(String[] args) {
        int hasil = 0;
        for(int i = 1; i <= 20; i++) {
            hasil = hasil + i;
        } System.out.println(hasil);
    }
}

/* OUTPUT
210

// 1+2+3+...+20 = 210
*/
